<h3>About the project</h3>

<p>We may live half way around the world from each other, but only a click away. Today, despite being under the constant watch of state censors, the Chinese-language web hosts over 100 million blogs, written by some <a href="http://www.chinadaily.com.cn/china/2009-01/06/content_7372280.htm" target="_new">50 million individuals</a>.</p>

<p>What are they interested in? What are their dreams and fears? What lives do they lead? Even though China and its people are the <a href="http://www.languagemonitor.com/news/top-news-stories-of-the-decade/" target="_new">most written about topic</a> in the English-language media, who knows? For non-Chinese speakers, “It’s Chinese To Me” is an experiment in listening to Chinese women speak in their own words, without the mediation of a foreign interpreter.</p>

<p>What’s with <a href="http://www.omniglot.com/language/idioms/incomprehensible.php" target="_new">the title</a>, you ask?</p>

